#ifndef FUELGAUGE_H
#define FUELGAUGE_H

// Class Declaration - Fuel gauge
class FuelGauge{
private:
    const int TANKMAX = 22;         // Max number of gallons can hold
    int gasLevel = 0;                       // Level of gas in the tank
    
    
public:
    int getGas();
    /**************************************************************************************************************************
     *  getGas Function
     * --------------------------------------------------------------------------------------------------------------------------------------------
     * The purpose of this function is to receive the current level of the gas tank in gallons.
     *************************************************************************************************************************/
    
    void incrementGas();
    /**************************************************************************************************************************
     *  incrementGas Function
     * --------------------------------------------------------------------------------------------------------------------------------------------
     * The purpose of this function is to increment the gas by 1 until it has reached the maximum tank size.
     *************************************************************************************************************************/
    
    void decrementGas();
    /**************************************************************************************************************************
     *  decrementGas Function
     * --------------------------------------------------------------------------------------------------------------------------------------------
     * The purpose of this function is to decrement the gas level by 1 until it reaches 0.
     *************************************************************************************************************************/
};

#endif /* FUELGAUGE_H */

